/// Контракт движка вычисления выражений ("a > 5", "name == 'Bob'" и т.п.).
///
/// Реализация выбирается приложением. В Flutter-стэке используется адаптер
/// над пакетом `expressions`, но ядро не зависит от конкретной реализации.
abstract class ScriptExpressionEngine {
  /// Вычислить выражение в указанном контексте.
  /// [context] — переменные/значения, доступные выражению.
  /// Может бросить [FormatException] при невалидном выражении или
  /// [Exception] в случае невозможности оценить.
  dynamic evaluate(String expression, Map<String, dynamic> context);
}
